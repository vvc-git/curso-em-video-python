x = input("Type something: ")
print('The type primitive of x is', type(x))
print('There is a space?',x.isspace())
print('Is it a number?',x.isnumeric())
print('Is it a letter?',x.isalpha())
print('Is it an alphanumeric? ',x.isalnum())
print('Is it capital letter? ',x.isupper())
print('Is it lowerrcase?',x.islower())
print ('Is it title? ',x.istitle())


